def generate_colorado_grid(lat_step_km=20, lon_step_km=20):
    """
    Generates a list of (lat, lon) points spaced approximately `lat_step_km` x `lon_step_km` km apart
    within the bounding box of Colorado, excluding overshoots.
    """

    import numpy as np

    # Colorado bounding box (approximate)
    min_lat, max_lat = 37.0, 41.0
    min_lon, max_lon = -109.0, -102.0

    # Convert step sizes from km to degrees
    lat_step_deg = lat_step_km / 111.0
    avg_lat = (min_lat + max_lat) / 2
    lon_km_per_deg = 111.0 * np.cos(np.radians(avg_lat))
    lon_step_deg = lon_step_km / lon_km_per_deg

    # Create grid without overshooting
    lat_points = np.arange(min_lat, max_lat, lat_step_deg)
    lon_points = np.arange(min_lon, max_lon, lon_step_deg)

    # Cartesian product of lat/lon
    grid = [(round(lat, 6), round(lon, 6)) for lat in lat_points for lon in lon_points]
    return grid


